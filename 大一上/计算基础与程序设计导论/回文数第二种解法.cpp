#include<stdio.h>
int main()
{
	long long n,l,p,a,b,x,length;
	scanf("%lld",&n);
	l=1;
	p=10;
	x=0;
	while(n/p!=0)
	{
		p=p*10;
		l++; 
	}
	length=l;
	if(length%2==0)
	{
		while(length>0)
		{
			a=n/(p/10);
			b=n%10;
			length=length-2;
			n=n/10-a*(p/100);
			p=p/100;
			if(a==b)
			x++;
		}
		if(x==l/2)
		printf("Yes");
		else
		printf("No");
	}
	else
	{
		while(length>1)
		{
			a=n/(p/10);
			b=n%10;
			length=length-2;
			n=n/10-a*(p/100);
			p=p/100;
			if(a==b)
			x++;
		}
		if(x==(l-1)/2)
		printf("Yes");
		else
		printf("No");
	}
	return 0;
}
